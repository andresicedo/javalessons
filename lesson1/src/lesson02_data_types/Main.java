package lesson02_data_types;

public class Main {

	public static void main(String[] args) {
		// 	Data types explained...
		//  Primitive data types - not changeable
		int hello = 21;//integer
		double world = 21.21;//double/float
		boolean bool = true; //boolean true/false
		char a = 'a';//character a-z 0-9 single quote one char
		// Not primitive
		String str = "andres"; //double quote any char is valid within ""
		
		System.out.println(hello);
		System.out.println(world);
		System.out.println(bool);
		System.out.println(a);
		System.out.println(str);
	}

}
